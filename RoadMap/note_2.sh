*** JavaScript BackEnd Developer RoadMap ***
** Phase 1 **  (The Core Foundation) ---------------------------
JavaScript  => ES6, Promises, Async/Await, Scope/Closures
Node.js     => Event Loop, Modules
NPM         => Package.json, dependencies, devDependencies
Terminal    => cd, ls, mkdir, touch, clear, exit, nano

** Phase 2 **  (Builging the "Brain" Express & APIs) -----------
Express.js  => Middleware, Routing, Request/Response
RESTful API => Http Methods, Status Codes (200, 201, 404, 500)
Tast API    => Postman, Insomnia, Httpie (for Terminal)
Validation  => Zod, Joi

** Phase 3 **  (Data & Security "The Professional Leap") -------
Databases   => MongoDB, MySQL,
Authentication & Authorization => JWT (JSON Web Tokens), (Bcrypt Module)
Environment Variables => .env, (Dotenv Module)

** Phase 4 **  (Modern Requirements)
Git, GitHub, Docker, TypeScript,
Deployment  => "Take an app live using" Render, Railway, or AWS (EC2/Lambda)
